/*
Условие задачи:
Сортировка массива по возрастанию методом слияния (Merge Sort).

Входные данные:
- Первая строка: число N (1 ≤ N ≤ 10⁵) — количество элементов.
- Вторая строка: массив из N целых чисел, разделённых пробелами.

Выходные данные:
- Выведите массив, отсортированный по возрастанию.
*/

package main

import (
	"bufio"
	"fmt"
	"io"
	"log"
	"os"
	"strconv"
	"strings"
)

// ============================================================
//  БЛОК 1: ПАРСИНГ СТРОКИ
// ============================================================

// parseLine преобразует строку с числами в срез целых чисел.
// Возвращает nil для пустых строк (чтобы их можно было пропустить).
func parseLine(line string) []int {
	// Удаляем пробелы и символы перевода строки по краям.
	line = strings.TrimSpace(line)
	// Если строка пустая — возвращаем nil.
	if line == "" {
		return nil
	}
	// Разбиваем строку на отдельные подстроки по пробелам.
	field := strings.Fields(line)
	// Создаём срез нужного размера.
	row := make([]int, len(field))
	// Преобразуем каждую подстроку в число.
	for j, val := range field {
		temp, err := strconv.Atoi(val)
		if err != nil {
			log.Fatal("convert error")
		}
		row[j] = temp
	}
	return row
}

// ============================================================
//  БЛОК 2: ИНИЦИАЛИЗАЦИЯ ДАННЫХ
// ============================================================

// initialisation читает файл и возвращает массив для сортировки.
func initialisation() (int, []int) {

	// ---------- 2.1: ПОЛУЧЕНИЕ АРГУМЕНТОВ КОМАНДНОЙ СТРОКИ ----------
	// Получаем индекс массива из аргументов командной строки.
	// Если аргумент не передан, используем индекс 1 (первый массив).
	index := 1
	if len(os.Args) > 1 {
		indx, err := strconv.Atoi(os.Args[1])
		if err != nil || indx < 0 {
			log.Fatal("wrong input")
		}
		index = indx
	}

	// ---------- 2.2: ОТКРЫТИЕ ФАЙЛА ----------
	file, err := os.Open("../../input.txt")
	if err != nil {
		log.Fatal("didn't open file")
	}
	defer file.Close()

	// ---------- 2.3: НАСТРОЙКА ВВОДА-ВЫВОДА ----------
	// Используем буферизированный ввод для эффективной работы с файлом.
	// Размер буфера 4096 байт — оптимально для небольших файлов.
	readerFile := bufio.NewReaderSize(file, 4096)

	// Буферизированный вывод: накапливает данные и отправляет их одной операцией ввода-вывода.
	// Это ускоряет работу при большом количестве fmt.Println.
	writer := bufio.NewWriterSize(os.Stdout, 1<<20)
	defer writer.Flush()

	// ---------- 2.4: ЧТЕНИЕ ДАННЫХ ИЗ ФАЙЛА ----------
	// matrix — хранилище для всех строк файла.
	// Каждая строка — отдельный срез целых чисел.
	var matrix [][]int

	// Читаем файл построчно до EOF (конца файла).
	for {
		line, err := readerFile.ReadString('\n')
		// Если ошибка не EOF — критическая, завершаем программу.
		if err != nil && err != io.EOF {
			log.Fatal("reading error")
		}
		// Парсим строку: удаляем пробелы, разбиваем на числа.
		row := parseLine(line)
		// Пропускаем пустые строки (parseLine возвращает nil).
		if row != nil {
			matrix = append(matrix, row)
		}
		// Если достигнут конец файла — выходим из цикла.
		if err == io.EOF {
			break
		}
	}

	// ---------- 2.5: ПОДГОТОВКА ДАННЫХ ----------
	// Первая строка файла — всегда N (количество элементов).
	n := matrix[0][0]

	// Берём нужный массив из файла.
	// Индекс можно менять в зависимости от того, какой массив нужно сортировать.
	// matrix[1] — первый массив, matrix[2] — второй, matrix[3] — третий и т.д.
	arr := matrix[index]

	// Защита от некорректных данных:
	// 1. Если в строке больше чисел, чем N — обрезаем до N.
	if len(arr) > n {
		arr = arr[:n]
	}
	// 2. Если в строке меньше чисел, чем N — программа не может работать.
	if len(arr) < n {
		log.Fatal("not enough elements in array")
	}
	return n, arr
}

// ============================================================
//  БЛОК 3: ГЛАВНАЯ ФУНКЦИЯ
// ============================================================

func main() {
	n, arr := initialisation()

	// Вывод исходного массива
	fmt.Println(arr)

	// Сортировка слиянием
	mergeSort(arr, 0, n-1)

	// Вывод отсортированного массива
	fmt.Println(arr)
}

// ============================================================
//  БЛОК 4: СОРТИРОВКА СЛИЯНИЕМ
// ============================================================

// merge — слияние двух отсортированных по возрастанию подмассивов.
// Параметры:
//
//	arr   - исходный массив
//	left  - начало левого подмассива
//	mid   - конец левого подмассива (включительно)
//	right - конец правого подмассива (включительно)
//
// Возвращает новый отсортированный срез.
func merge(arr []int, left, mid, right int) []int {
	// Размеры левого и правого подмассивов
	l1 := mid - left + 1
	l2 := right - mid

	// Срезы-представления левой и правой частей
	arr1 := arr[left : mid+1]
	arr2 := arr[mid+1 : right+1]

	// Результирующий массив
	result := make([]int, l1+l2)

	i, j, k := 0, 0, 0

	// [ОПТИМИЗАЦИЯ] Быстрый путь: если все элементы arr1 меньше всех элементов arr2
	if arr1[l1-1] <= arr2[0] {
		copy(result, arr1)
		copy(result[l1:], arr2)
		return result
	}

	// [ОПТИМИЗАЦИЯ] Быстрый путь: если все элементы arr2 меньше всех элементов arr1
	if arr2[l2-1] <= arr1[0] {
		copy(result, arr2)
		copy(result[l2:], arr1)
		return result
	}

	// ----- Обычное слияние с пошаговым сравнением -----
	if arr1[0] < arr2[0] {
		// Случай, когда первый элемент левого меньше первого элемента правого
		for i < l1 && j < l2 {
			if arr1[i] < arr2[j] {
				result[k] = arr1[i]
				i++
				k++
			} else {
				result[k] = arr2[j]
				j++
				k++
			}
		}
		// Копируем остатки из того массива, который ещё не закончился
		if i < l1 {
			copy(result[k:], arr1[i:])
		} else {
			copy(result[k:], arr2[j:])
		}
	} else {
		// Случай, когда первый элемент правого меньше или равен первому элементу левого
		for i < l1 && j < l2 {
			if arr2[j] < arr1[i] {
				result[k] = arr2[j]
				j++
				k++
			} else {
				result[k] = arr1[i]
				i++
				k++
			}
		}
		// Копируем остатки из того массива, который ещё не закончился
		if j < l2 {
			copy(result[k:], arr2[j:])
		} else {
			copy(result[k:], arr1[i:])
		}
	}

	return result
}

// mergeSort — рекурсивная сортировка слиянием участка arr[left..right] по возрастанию.
func mergeSort(arr []int, left, right int) {
	// Базовый случай: если участок пуст или состоит из одного элемента — выходим.
	if left >= right {
		return
	}

	// [ОПТИМИЗАЦИЯ] Если в участке 2 элемента — сортируем их напрямую (без рекурсии).
	if right-left == 1 {
		if arr[left] > arr[right] {
			arr[left], arr[right] = arr[right], arr[left]
		}
		return
	}

	// Находим середину и рекурсивно сортируем левую и правую половины.
	mid := left + (right-left)/2
	mergeSort(arr, left, mid)
	mergeSort(arr, mid+1, right)

	// Сливаем две отсортированные половины и копируем результат обратно.
	sorted := merge(arr, left, mid, right)
	copy(arr[left:right+1], sorted)
}

