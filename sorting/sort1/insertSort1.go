/*
Название: Сортировка вставками (Insertion Sort)
Легенда:
Вам дан массив целых чисел. Необходимо отсортировать его по возрастанию, используя алгоритм сортировки вставками.
Ваша задача — не просто написать код, но и показать, что вы понимаете, как именно алгоритм перекладывает элементы.
Для этого программа должна выводить состояние массива после каждой вставки (каждого прохода внешнего цикла).

ОСОБЕННОСТИ ДАННОЙ РЕАЛИЗАЦИИ:
Данный код использует АЛЬТЕРНАТИВНЫЙ подход сортировки вставками с РАЗДЕЛЕННЫМИ ЭТАПАМИ:
- Левая часть массива (индексы 0..i1-1) всегда отсортирована
- На каждом шаге мы берем элемент arr[i1] и вставляем его в правильную позицию
- ПОИСК места и СДВИГ элементов разделены на два отдельных цикла

ОТЛИЧИЕ ОТ КЛАССИЧЕСКОЙ РЕАЛИЗАЦИИ (ОДИН ЦИКЛ):
В данной реализации мы разделяем логику на три четких этапа:
1. Проверка: нужна ли вставка (if arr[i1-1] > arr[i1])
2. Поиск места для вставки (цикл for i2)
3. Сдвиг элементов вправо через swap (цикл for i3)

Преимущества данной реализации:
- Логика разделена на понятные этапы
- Легче понять и отлаживать
- Наглядно показывает все шаги алгоритма
- Поиск места выполняется слева направо (нестандартный подход)

Недостатки:
- Медленнее классической реализации (два цикла вместо одного)
- Больше кода
- Использует swap вместо сдвига (медленнее)

Сложность алгоритма:
- Лучший случай: O(n) — массив уже отсортирован
- Средний случай: O(n²)
- Худший случай: O(n²) — массив отсортирован в обратном порядке
- Память: O(1) — сортировка на месте
- Стабильность: ДА (порядок равных элементов сохраняется)

ОСОБЕННОСТЬ ПОИСКА:
В данной реализации ПОИСК места выполняется СЛЕВА НАПРАВО (от 0 до i1-1),
в то время как в классической реализации поиск идет СПРАВА НАЛЕВО (от i1-1 до 0).
Это делает код более понятным для начинающих, но менее эффективным.

Формат входных данных:
В первой строке задается одно целое число N (1 ≤ N ≤ 1000) — количество элементов в массиве.
Во второй строке вводится N целых чисел, разделенных пробелами.

Формат выходных данных:
Программа должна вывести N строк.
В i-й строке (где i — номер шага, начиная с 1) должен быть выведен массив после того, как i-й по счету элемент (начиная со второго)
был вставлен на свою позицию в отсортированной части массива.
*/
package main

import (
	"bufio"
	"fmt"
	"io"
	"log"
	"os"
	"strconv"
	"strings"
)

func main() {
	// ============================================
	// БЛОК 1: ОТКРЫТИЕ ФАЙЛА
	// ============================================
	file, err := os.Open("input.txt")
	if err != nil {
		log.Fatal("didn't open file")
		return
	}
	defer file.Close()

	// ============================================
	// БЛОК 2: ЧТЕНИЕ ДАННЫХ
	// ============================================
	// Используем буферизированный ввод для эффективной работы с файлом.
	// Размер буфера 4096 байт — оптимально для небольших файлов.
	readerFile := bufio.NewReaderSize(file, 4096)

	// Буферизированный вывод: накапливает данные и отправляет их одной операцией ввода-вывода.
	// Это ускоряет работу при большом количестве fmt.Println.
	writer := bufio.NewWriterSize(os.Stdout, 1<<20)
	defer writer.Flush()

	// matrix — хранилище для всех строк файла.
	// Каждая строка — отдельный слайс целых чисел.
	var matrix [][]int

	// Читаем файл построчно до EOF (конца файла).
	for {
		line, err := readerFile.ReadString('\n')
		// Если ошибка не EOF — критическая, завершаем программу.
		if err != nil && err != io.EOF {
			log.Fatal("reading error")
		}
		// Парсим строку: удаляем пробелы, разбиваем на числа.
		row := parseLine(line)
		// Пропускаем пустые строки (parseLine возвращает nil).
		if row != nil {
			matrix = append(matrix, row)
		}
		// Если достигнут конец файла — выходим из цикла.
		if err == io.EOF {
			break
		}
	}

	// ============================================
	// БЛОК 3: ПОДГОТОВКА ДАННЫХ
	// ============================================
	// Первая строка файла — всегда N (количество элементов).
	n := matrix[0][0]

	// Берем нужный массив из файла.
	// Индекс можно менять в зависимости от того, какой массив нужно сортировать.
	// matrix[1] — первый массив, matrix[2] — второй, matrix[3] — третий и т.д.
	arr := matrix[1]

	// Защита от некорректных данных:
	// 1. Если в строке больше чисел, чем N — обрезаем до N.
	if len(arr) > n {
		arr = arr[:n]
	}
	// 2. Если в строке меньше чисел, чем N — программа не может работать.
	if len(arr) < n {
		log.Fatal("not enough elements in array")
	}

	// ============================================
	// БЛОК 4: СОРТИРОВКА ВСТАВКАМИ (РАЗДЕЛЕННЫЕ ЭТАПЫ)
	// ============================================
	// Переменные вынесены наружу для переиспользования памяти.
	// key — вставляемый элемент, keyPosition — найденная позиция для вставки.
	var key, keyPosition int

	// Внешний цикл: начинаем со второго элемента (индекс 1),
	// потому что первый элемент (индекс 0) считается уже отсортированным.
	for i1 := 1; i1 < n; i1++ {
		// ============================================================
		// ЭТАП 1: ПРОВЕРКА — НУЖНА ЛИ ВСТАВКА?
		// ============================================================
		// Если предыдущий элемент меньше или равен текущему,
		// то текущий элемент уже на своем месте в отсортированной части.
		// В этом случае мы пропускаем вставку.
		if arr[i1-1] > arr[i1] {
			// Запоминаем текущий элемент, который будем вставлять.
			key = arr[i1]
			// Предполагаем, что элемент останется на своем месте.
			// Если в цикле поиска найдется элемент больше key,
			// позиция будет скорректирована.
			keyPosition = i1

			// ============================================================
			// ЭТАП 2: ПОИСК МЕСТА ДЛЯ ВСТАВКИ (СЛЕВА НАПРАВО)
			// ============================================================
			// ОСОБЕННОСТЬ: поиск выполняется СЛЕВА НАПРАВО (от 0 до i1-1).
			// В классической реализации поиск идет СПРАВА НАЛЕВО.
			//
			// Почему слева направо?
			// - Такой подход более нагляден для понимания
			// - Легче отследить, где именно нужно вставить элемент
			// - Медленнее, но проще для обучения
			//
			// Ищем первый элемент, который больше key.
			// Именно перед ним нужно вставить key.
			for i2 := 0; i2 < i1; i2++ {
				if arr[i2] > key {
					keyPosition = i2
					break // Выходим, когда нашли место
				}
			}

			// ============================================================
			// ЭТАП 3: СДВИГ ЭЛЕМЕНТОВ ВПРАВО (ЧЕРЕЗ SWAP)
			// ============================================================
			// Сдвигаем элементы от i1 до keyPosition вправо.
			// Используем swap (обмен) вместо сдвига, чтобы код был понятнее.
			//
			// Как это работает:
			// Идем от текущей позиции (i1) влево до найденного места (keyPosition)
			// и меняем местами соседние элементы.
			// В результате key "просачивается" влево на нужную позицию.
			for i3 := i1; i3 > keyPosition; i3-- {
				arr[i3], arr[i3-1] = arr[i3-1], arr[i3]
			}
			// ВНИМАНИЕ: в этой реализации НЕТ явной вставки arr[keyPosition] = key,
			// потому что key уже находится на своем месте благодаря обменам.
			// Это главное отличие от классической реализации со сдвигом.
		}

		// Выводим состояние массива после каждой итерации.
		// Это требование задачи и одновременно визуальная проверка алгоритма.
		fmt.Println(arr)
	}
}

// ============================================
// ВСПОМОГАТЕЛЬНАЯ ФУНКЦИЯ
// ============================================
// parseLine преобразует строку с числами в слайс целых чисел.
// Возвращает nil для пустых строк (чтобы их можно было пропустить).
func parseLine(line string) []int {
	// Удаляем пробелы и символы перевода строки по краям.
	line = strings.TrimSpace(line)
	// Если строка пустая — возвращаем nil.
	if line == "" {
		return nil
	}
	// Разбиваем строку на отдельные подстроки по пробелам.
	field := strings.Fields(line)
	// Создаем слайс нужного размера.
	row := make([]int, len(field))
	// Преобразуем каждую подстроку в число.
	for j, val := range field {
		temp, err := strconv.Atoi(val)
		if err != nil {
			log.Fatal("convert error")
		}
		row[j] = temp
	}
	return row
}