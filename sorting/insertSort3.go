/*
Название: Сортировка вставками (Insertion Sort)
Легенда:
Вам дан массив целых чисел. Необходимо отсортировать его по возрастанию, используя алгоритм сортировки вставками.
Ваша задача — не просто написать код, но и показать, что вы понимаете, как именно алгоритм перекладывает элементы.
Для этого программа должна выводить состояние массива после каждой вставки (каждого прохода внешнего цикла).

ОСОБЕННОСТИ ДАННОЙ РЕАЛИЗАЦИИ:
Данный код использует классический подход сортировки вставками:
- Левая часть массива (индексы 0..i-1) всегда отсортирована
- На каждом шаге мы берем элемент arr[i] и вставляем его в правильную позицию
- Внутренний цикл сдвигает элементы вправо, освобождая место для вставки

ОТЛИЧИЕ ОТ АЛЬТЕРНАТИВНОЙ РЕАЛИЗАЦИИ (ЧЕРЕЗ SWAP):
В данной реализации мы используем СДВИГ (arr[j+1] = arr[j]), а не ОБМЕН (swap).
Это делает алгоритм быстрее, так как:
- Сдвиг: 1 операция записи в память
- Swap: 3 операции записи в память (через временную переменную)

Преимущества данной реализации:
- Быстрее на больших данных
- Меньше операций записи в память
- Классический вариант, который ожидают увидеть на собеседовании

Сложность алгоритма:
- Лучший случай: O(n) — массив уже отсортирован
- Средний случай: O(n²)
- Худший случай: O(n²) — массив отсортирован в обратном порядке
- Память: O(1) — сортировка на месте
- Стабильность: ДА (порядок равных элементов сохраняется)

Где применяется:
- В составе гибридных алгоритмов (Timsort, Introsort)
- Для сортировки небольших массивов (до 10-20 элементов)
- Когда данные почти отсортированы

Формат входных данных:
В первой строке задается одно целое число N (1 ≤ N ≤ 1000) — количество элементов в массиве.
Во второй строке вводится N целых чисел, разделенных пробелами.

Формат выходных данных:
Программа должна вывести N строк.
В i-й строке (где i — номер шага, начиная с 1) должен быть выведен массив после того, как i-й по счету элемент (начиная со второго)
был вставлен на свою позицию в отсортированной части массива.
*/
package main

import (
	"bufio"
	"fmt"
	"io"
	"log"
	"os"
	"strconv"
	"strings"
	"path/filepath"
)

func main() {
	// ============================================
	// БЛОК 1: ОТКРЫТИЕ ФАЙЛА
	// ============================================
	index:=1
	if len(os.Args)>1{
		indx,err:=strconv.Atoi(os.Args[1])
		if err!=nil||indx<0{
			log.Fatal("wrong input")	
		}
		index=indx	
	}
path := filepath.Join("..", "..","input.txt")
file, err := os.Open(path)
	if err != nil {
		log.Fatal("didn't open file")
		return
	}
	defer file.Close()

	// ============================================
	// БЛОК 2: ЧТЕНИЕ ДАННЫХ
	// ============================================
	// Используем буферизированный ввод для эффективной работы с файлом.
	// Размер буфера 4096 байт — оптимально для небольших файлов.
	readerFile := bufio.NewReaderSize(file, 4096)

	// Буферизированный вывод: накапливает данные и отправляет их одной операцией ввода-вывода.
	// Это ускоряет работу при большом количестве fmt.Println.
	writer := bufio.NewWriterSize(os.Stdout, 1<<20)
	defer writer.Flush()

	// matrix — хранилище для всех строк файла.
	// Каждая строка — отдельный слайс целых чисел.
	var matrix [][]int

	// Читаем файл построчно до EOF (конца файла).
	for {
		line, err := readerFile.ReadString('\n')
		// Если ошибка не EOF — критическая, завершаем программу.
		if err != nil && err != io.EOF {
			log.Fatal("reading error")
		}
		// Парсим строку: удаляем пробелы, разбиваем на числа.
		row := parseLine(line)
		// Пропускаем пустые строки (parseLine возвращает nil).
		if row != nil {
			matrix = append(matrix, row)
		}
		// Если достигнут конец файла — выходим из цикла.
		if err == io.EOF {
			break
		}
	}

	// ============================================
	// БЛОК 3: ПОДГОТОВКА ДАННЫХ
	// ============================================
	// Первая строка файла — всегда N (количество элементов).
	n := matrix[0][0]

	// Берем нужный массив из файла.
	// Индекс можно менять в зависимости от того, какой массив нужно сортировать.
	// matrix[1] — первый массив, matrix[2] — второй, matrix[3] — третий и т.д.
	arr := matrix[index]

	// Защита от некорректных данных:
	// 1. Если в строке больше чисел, чем N — обрезаем до N.
	if len(arr) > n {
		arr = arr[:n]
	}
	// 2. Если в строке меньше чисел, чем N — программа не может работать.
	if len(arr) < n {
		log.Fatal("not enough elements in array")
	}

	// ============================================
	// БЛОК 4: СОРТИРОВКА ВСТАВКАМИ (КЛАССИЧЕСКАЯ РЕАЛИЗАЦИЯ)
	// ============================================
	// Переменная key вынесена наружу для переиспользования памяти.
	// Это микро-оптимизация: key создается один раз, а не на каждой итерации.
	var key int

	// Внешний цикл: начинаем со второго элемента (индекс 1),
	// потому что первый элемент (индекс 0) считается уже отсортированным.
	for i := 1; i < n; i++ {
		// j — указатель на предыдущий элемент, с которого начинаем сравнение.
		j := i - 1
		// Запоминаем текущий элемент, который будем вставлять.
		key = arr[i]

		// ВНУТРЕННИЙ ЦИКЛ: СДВИГ ЭЛЕМЕНТОВ ВПРАВО
		// Классический подход: вместо обмена (swap) мы сдвигаем элементы.
		// Это быстрее, так как требует меньше операций записи в память.
		//
		// Условия выхода:
		// 1. j < 0 — дошли до начала массива.
		// 2. arr[j] <= key — нашли место для вставки (элемент не больше key).
		for j >= 0 && arr[j] > key {
			// Сдвигаем текущий элемент вправо, освобождая место для key.
			// Это ОТЛИЧАЕТ данную реализацию от варианта через swap,
			// где мы бы меняли местами arr[j] и arr[j+1].
			arr[j+1] = arr[j]
			// Переходим к следующему элементу слева.
			j--
		}
		// Вставляем key на освободившееся место (j+1).
		// Почему j+1? Потому что после последнего j-- мы оказались на позиции,
		// где arr[j] <= key, а значит key должен стоять сразу справа от j.
		arr[j+1] = key

		// Выводим состояние массива после каждой вставки.
		// Это требование задачи и одновременно визуальная проверка алгоритма.
		fmt.Println(arr)
	}
}

// ============================================
// ВСПОМОГАТЕЛЬНАЯ ФУНКЦИЯ
// ============================================
// parseLine преобразует строку с числами в слайс целых чисел.
// Возвращает nil для пустых строк (чтобы их можно было пропустить).
func parseLine(line string) []int {
	// Удаляем пробелы и символы перевода строки по краям.
	line = strings.TrimSpace(line)
	// Если строка пустая — возвращаем nil.
	if line == "" {
		return nil
	}
	// Разбиваем строку на отдельные подстроки по пробелам.
	field := strings.Fields(line)
	// Создаем слайс нужного размера.
	row := make([]int, len(field))
	// Преобразуем каждую подстроку в число.
	for j, val := range field {
		temp, err := strconv.Atoi(val)
		if err != nil {
			log.Fatal("convert error")
		}
		row[j] = temp
	}
	return row
}